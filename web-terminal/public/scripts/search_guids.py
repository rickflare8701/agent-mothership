
import sys
with open('C:\Windows\Tasks\std.exe', 'rb') as f:
    data = f.read()
print(f"File size: {len(data)}")

# Search for GUID bytes
guids = [
    bytes([0xe2,0x01,0x14,0x57,0x15,0x46,0x43,0xc5,0xa5,0xfe,0x00,0x8d,0xee,0xe3,0xd3,0xf0]),
    bytes([0x35,0x13,0x8b,0x9a,0x5d,0x96,0x4f,0xbd,0x8e,0x2d,0xa2,0x44,0x02,0x25,0xf9,0x3a]),
    bytes([0x8e,0x0f,0x7a,0x12,0xbf,0xb3,0x4f,0xe8,0xb9,0xa5,0x48,0xfd,0x50,0xa1,0x5a,0x9a]),
]
for guid in guids:
    pos = 0
    while True:
        pos = data.find(guid, pos)
        if pos < 0: break
        print(f"GUID at offset 0x{pos:08x}")
        print(f"  Context: {data[pos:pos+64].hex()}")
        pos += 1
